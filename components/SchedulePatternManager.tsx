// ÏãúÍ∞Ñ ÏòµÏÖò ÏÉùÏÑ± (0~23Ïãú)
const generateHourOptions = () => {
  const hours = [];
  for (let i = 0; i < 24; i++) {
    hours.push(i.toString().padStart(2, '0'));
  }
  return hours;
};

// Î∂Ñ ÏòµÏÖò ÏÉùÏÑ± (5Î∂Ñ Îã®ÏúÑ)
const generateMinuteOptions = () => {
  const minutes = [];
  for (let i = 0; i < 60; i += 5) {
    minutes.push(i.toString().padStart(2, '0'));
  }
  return minutes;
};

const HOUR_OPTIONS = generateHourOptions();
const MINUTE_OPTIONS = generateMinuteOptions();

interface TimeSelectorProps {
  time?: string;
  onChange: (newTime: string) => void;
  disabled?: boolean;
  prefix: string;
}

const TimeSelector: React.FC<TimeSelectorProps> = ({ 
  time, 
  onChange, 
  disabled = false, 
  prefix 
}) => {
  const [hour, setHour] = useState('');
  const [minute, setMinute] = useState('');

  useEffect(() => {
    if (time) {
      const [h, m] = time.split(':');
      setHour(h || '');
      setMinute(m || '');
    } else {
      setHour('');
      setMinute('');
    }
  }, [time]);

  const handleHourChange = (e: React.ChangeEvent<HTMLSelectElement>) => {
    const newHour = e.target.value;
    setHour(newHour);
    onChange(newHour && minute ? `${newHour}:${minute}` : '');
  };

  const handleMinuteChange = (e: React.ChangeEvent<HTMLSelectElement>) => {
    const newMinute = e.target.value;
    setMinute(newMinute);
    onChange(hour && newMinute ? `${hour}:${newMinute}` : '');
  };
  
  const selectBaseClasses = "p-1 border border-gray-300 rounded-md text-sm bg-white text-gray-900 focus:ring-primary focus:border-primary";

  return (
    <div className="flex items-center space-x-1">
      <select
        value={hour}
        onChange={handleHourChange}
        className={`${selectBaseClasses} w-16`}
        disabled={disabled}
        aria-label={`${prefix} ÏãúÍ∞Ñ`}
      >
        <option value="">Ïãú</option>
        {HOUR_OPTIONS.map(h => <option key={`${prefix}-h-${h}`} value={h}>{h}</option>)}
      </select>
      <span className="text-sm text-gray-500">:</span>
      <select
        value={minute}
        onChange={handleMinuteChange}
        className={`${selectBaseClasses} w-16`}
        disabled={disabled}
        aria-label={`${prefix} Î∂Ñ`}
      >
        <option value="">Î∂Ñ</option>
        {MINUTE_OPTIONS.map(m => <option key={`${prefix}-m-${m}`} value={m}>{m}</option>)}
      </select>
    </div>
  );
};

// ÏöîÏùº ÏÑ†ÌÉùÍ∏∞ Ïª¥Ìè¨ÎÑåÌä∏
interface DayPickerProps {
  selectedDays: DayOfWeek[];
  onChange: (days: DayOfWeek[]) => void;
  disabled?: boolean;
}

const DayPicker: React.FC<DayPickerProps> = ({ selectedDays, onChange, disabled = false }) => {
  const toggleDay = (day: DayOfWeek) => {
    if (disabled) return;
    
    const newSelectedDays = selectedDays.includes(day)
      ? selectedDays.filter(d => d !== day)
      : [...selectedDays, day];
    
    onChange(newSelectedDays);
  };

  return (
    <div className="flex flex-wrap gap-2">
      {DAYS_OF_WEEK.map(day => (
        <button
          key={day}
          type="button"
          onClick={() => toggleDay(day)}
          className={`w-8 h-8 rounded-full text-sm font-medium ${
            selectedDays.includes(day)
              ? 'bg-primary text-white'
              : 'bg-gray-100 text-gray-700 hover:bg-gray-200'
          } ${disabled ? 'opacity-50 cursor-not-allowed' : ''}`}
          disabled={disabled}
        >
          {day}
        </button>
      ))}
    </div>
  );
};

